package Packages;

public class Main {
    public static void main(String[] args) {
        System.out.println("Let's learn about packages in java!!!");

        /* packages: We can compare package with a folder in a file directory. A package in java is used to group
            related classes as well as to avoid name conflicts, and to write a better maintainable codes.

            There are two types of packages:
            => User Defined
            => In-built

            In-built packages:
            -> lang : It contains the essential stuffs(java language specific) of java. It imports automatically
            -> io: It contains input/output classes. The classes that need to read a file uses this package
            -> util: It contains utility classes(Data Structures & collections).
            -> applet: It used more on the development side to create applets in java.
            -> awt: It used to create GUI(Graphical User Interface)
            -> net: It used perform networking operations.
         */
    }
}
